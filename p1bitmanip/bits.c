int implication(int x, int y) {
	return (!x) | ~(~x | ~y);
}
/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3

 
 */
int isAsciiDigit(int x) {
	int x_sign = x>>31 & 1; // if x is negative, it is definitely not in the range
	int lower_x = 0x2F + (~x+1);
	int x_upper = x + (~0x3A+1);
	int msb1 = lower_x>>31 & 1;
	int msb2 = x_upper>>31 & 1;
	return (!x_sign) & (msb1 & msb2);
}
