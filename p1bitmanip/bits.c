/* 
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
	int temp = x>>31;
	return (x^temp)+(~temp+1);
}
/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
	int x_sign = x>>31 & 1;
	int y_sign = y>>31 & 1;
	int sum_sign = (x + y)>>31 & 1;
	int diff_sign = x_sign ^ y_sign; // test if x and y have different signs
	int diff_sum_sign = sum_sign^x_sign; // test if the sign of the sum is the same as sign of x
					     // given that x and y hasve the same sign
	return !((!diff_sign) & diff_sum_sign);
}
