/*
 * isLess - if x < y  then return 1, else return 0
 *   Example: isLess(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLess(int x, int y) {
	int diff = y + (~x + 1); // get y-x when x and y have the same msb
	int diff_sign = diff>>31 & 1;
	int x_sign = x>>31 & 1;
	int y_sign = y>>31 & 1;
	return (x_sign & !y_sign) | (!diff_sign & !(x_sign + ~y_sign + 1) & !!diff);
}
