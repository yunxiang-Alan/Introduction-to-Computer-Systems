/*
 * isPower2 - returns 1 if x is a power of 2, and 0 otherwise
 *   Examples: isPower2(5) = 0, isPower2(8) = 1, isPower2(0) = 0
 *   Note that no negative number is a power of 2.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int isPower2(int x) {
	int xminus = x+(~1+1); // use the fact that if x is power of two
	//then x-1 would change the 1 bit to 0, and vice versa
	return !!x & !(x>>31 & 1) & !(x & xminus);
}
